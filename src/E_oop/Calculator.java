package E_oop;

public class Calculator {
	double plus(double a, double b){
		return a+b;
	}
	
	double minus(double a, double b){
		return a-b;
	}
	
	double mul(double a, double b){
		return a*b;
	}
	
	double div(double a, double b){
		return a/b;
	}
	
	double _else(double a, double b){
		return a%b;
	}
	
	
}






